    private void btnSilenceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilenceActionPerformed
        // TODO add your handling code here:
        if(btnSilence.getText().equals("全体禁言"))
        {
            
            if (SendMessage("M_Silence")) {
                if(ReceiveMessage().getType().equalsIgnoreCase("M_SUCCESS"))
                {
                    btnSilence.setText("解除全体禁言");
                    parentUI.txtArea.append("您发起了全体禁言...\n");
                }
                
            }
        }
        else if(btnSilence.getText().equals("解除全体禁言"))
        {
            if(SendMessage("M_Speak"))
            {
                if(ReceiveMessage().getType().equalsIgnoreCase("M_SUCCESS"))
                {
                    btnSilence.setText("全体禁言");
                    parentUI.txtArea.append("您取消了全体禁言...\n");
                }
                
            }
        }
    }//GEN-LAST:event_btnSilenceActionPerformed

    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        if(SendMessage("M_READY"))
        {
            Message backMessage;
            if((backMessage=ReceiveMessage()).getType().equalsIgnoreCase("M_SUCCESS"))
            {
                if(signupUI==null)
                {
                    int onlineNumber=Integer.parseInt(backMessage.getText());
                    signupUI=new TeacherSignUpUI(onlineNumber,0,this);
                    signupUI.setVisible(true);
                    
                }
                
            }
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

