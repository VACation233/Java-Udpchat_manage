    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        // TODO add your handling code here:
        if(SendMessage("M_QUERY"))
        {
            if (ReceiveMessage().getType().equalsIgnoreCase("M_SUCCESS")) {
                resultTableUI = new ResultTableUI(btnQuery.getText(), this, parentUI.sqlHelper);
                DefaultTableModel tableModel = parentUI.sqlHelper.GenerateTableModel(btnQuery.getText());
                if (tableModel != null) {
                    resultTableUI.setTableModel(tableModel);
                }

                resultTableUI.setVisible(true);
            }

        }
        else
        {
            JOptionPane.showConfirmDialog(null, "服务器好像有点开小差...");
        }
        
        
        
        
    }//GEN-LAST:event_btnQueryActionPerformed

